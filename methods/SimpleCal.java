class SimpleCal{


	public static void main(String cal[])
	{
		System.out.println(add(2,4));
		sub(2,4);
		mul(2,4);
	}

	public static int add(int n1, int n2) {
		return n1+n2;
	}

	public static void sub(int n1, int n2) {
		System.out.println(n1-n2);
	}

	public static void mul(int n1, int n2) {
		System.out.println(n1*n2);
	}




}